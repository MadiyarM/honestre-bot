def _build_markup(options: list[str] | None, allow_back: bool) -> ReplyKeyboardMarkup | None:
    rows: list[list[str]] = []
    if options:
        rows.append(options)
    if allow_back:
        rows.append(["–ù–∞–∑–∞–¥"])
    return ReplyKeyboardMarkup(rows, resize_keyboard=True, one_time_keyboard=True) if rows else None

